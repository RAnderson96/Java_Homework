public class Calculator {
    public Calculator() {
    }
    public int addValues(int val1, int val2){
        return val1 + val2;
    }
    public int subtractValues(int val1, int val2){
        return val1 - val2;
    }
    public double divideValues(double val1, double val2){
        return (double) (val1/val2);
    }
}
